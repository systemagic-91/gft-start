package desafio.projeto.modulo4.banco;

public class Telefone {
    public int ddi;
    public int ddd;
    public int numero;

    public int getDdi() {
        return ddi;
    }

    public int getDdd() {
        return ddd;
    }

    public int getNumero() {
        return numero;
    }

    public void setDdi(int ddi) {
        this.ddi = ddi;
    }

    public void setDdd(int ddd) {
        this.ddd = ddd;
    }

    public void setNumero(int numero) {
        this.numero = numero;
    }
}
